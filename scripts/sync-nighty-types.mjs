import { mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { basename, extname, join } from 'node:path';

const sourceDir = '/home/steal/Resources/nighty/discord/types';
const targetDir = '/home/steal/Code/Discord/self-botting/src/types/nighty';

mkdirSync(targetDir, { recursive: true });

const files = readdirSync(sourceDir).filter(file => extname(file) === '.py');

function splitTopLevel(value) {
  const items = [];
  let current = '';
  let depth = 0;

  for (const char of value) {
    if (char === '[' || char === '(') depth += 1;
    if (char === ']' || char === ')') depth -= 1;

    if (char === ',' && depth === 0) {
      const trimmed = current.trim();
      if (trimmed) items.push(trimmed);
      current = '';
      continue;
    }

    current += char;
  }

  const trimmed = current.trim();
  if (trimmed) items.push(trimmed);
  return items;
}

function toTsType(rawType) {
  let value = rawType.trim();

  if (value.startsWith('NotRequired[') && value.endsWith(']')) {
    value = value.slice('NotRequired['.length, -1).trim();
  }

  if (value.startsWith('Required[') && value.endsWith(']')) {
    value = value.slice('Required['.length, -1).trim();
  }

  if (value.startsWith('Optional[') && value.endsWith(']')) {
    const inner = toTsType(value.slice('Optional['.length, -1));
    return `${inner} | null`;
  }

  if (value.startsWith('List[') && value.endsWith(']')) {
    const inner = toTsType(value.slice('List['.length, -1));
    return `${inner}[]`;
  }

  if (value.startsWith('Dict[') && value.endsWith(']')) {
    const [keyType, valueType] = splitTopLevel(value.slice('Dict['.length, -1));
    return `Record<${toTsType(keyType ?? 'string')}, ${toTsType(valueType ?? 'unknown')}>`;
  }

  if (value.startsWith('Union[') && value.endsWith(']')) {
    return splitTopLevel(value.slice('Union['.length, -1)).map(toTsType).join(' | ');
  }

  if (value.startsWith('Literal[') && value.endsWith(']')) {
    return splitTopLevel(value.slice('Literal['.length, -1)).join(' | ');
  }

  if (value === 'Any') return 'any';
  if (value === 'None') return 'null';
  if (value === 'str') return 'string';
  if (value === 'int') return 'number';
  if (value === 'float') return 'number';
  if (value === 'bool') return 'boolean';

  return value;
}

for (const fileName of files) {
  const sourcePath = join(sourceDir, fileName);
  const targetPath = join(targetDir, `${basename(fileName, '.py')}.d.ts`);

  const content = readFileSync(sourcePath, 'utf8').replace(/\r\n/g, '\n');
  const lines = content.split('\n');

  const output = [];
  output.push('// Auto-generated from nighty/discord/types Python definitions.');
  output.push('// Generated by scripts/sync-nighty-types.mjs');
  output.push('');

  const importedTypes = [];
  for (const line of lines) {
    const importMatch = line.match(/^from\s+\.(\w+)\s+import\s+(.+)$/);
    if (!importMatch) continue;

    const importSource = importMatch[1];
    const importList = importMatch[2]
      .split(',')
      .map(part => part.trim())
      .filter(Boolean)
      .join(', ');

    if (!importList) continue;
    importedTypes.push(`import type { ${importList} } from './${importSource}';`);
  }

  if (importedTypes.length > 0) {
    output.push(...importedTypes);
    output.push('');
  }

  const normalizedLines = [];
  for (let index = 0; index < lines.length; index += 1) {
    let line = lines[index];

    if (/^\w+\s*=\s*Literal\[$/.test(line.trim())) {
      const collected = [line.trim()];
      while (index + 1 < lines.length) {
        index += 1;
        const next = lines[index].trim();
        collected.push(next);
        if (next === ']') break;
      }

      line = collected.join(' ');
    }

    normalizedLines.push(line);
  }

  let i = 0;
  while (i < normalizedLines.length) {
    const line = normalizedLines[i]?.trim() ?? '';

    const aliasMatch = line.match(/^(\w+)\s*=\s*(Literal\[.*\]|Union\[.*\]|Optional\[.*\]|List\[.*\]|Dict\[.*\]|\w+)$/);
    if (aliasMatch) {
      const name = aliasMatch[1];
      const rhs = aliasMatch[2];
      output.push(`export type ${name} = ${toTsType(rhs)};`);
      i += 1;
      continue;
    }

    const classMatch = line.match(/^class\s+(\w+)\(([^)]*)\):$/);
    if (classMatch) {
      const interfaceName = classMatch[1];
      const baseRaw = classMatch[2];
      const baseParts = baseRaw
        .split(',')
        .map(part => part.trim())
        .filter(Boolean)
        .filter(part => part !== 'TypedDict' && part !== 'total=False' && part !== 'total=True');

      const header = baseParts.length
        ? `export interface ${interfaceName} extends ${baseParts.join(', ')} {`
        : `export interface ${interfaceName} {`;
      output.push(header);

      i += 1;
      while (i < normalizedLines.length) {
        const bodyLine = normalizedLines[i] ?? '';
        if (!bodyLine.startsWith('    ')) break;

        const fieldMatch = bodyLine.trim().match(/^(\w+):\s*(.+)$/);
        if (fieldMatch) {
          const fieldName = fieldMatch[1];
          const rawFieldType = fieldMatch[2].trim();

          const isOptional = rawFieldType.startsWith('NotRequired[');
          const tsType = toTsType(rawFieldType);
          output.push(`  ${fieldName}${isOptional ? '?' : ''}: ${tsType};`);
        }

        i += 1;
      }

      output.push('}');
      output.push('');
      continue;
    }

    i += 1;
  }

  const finalContent = `${output.join('\n').replace(/\n{3,}/g, '\n\n').trim()}\n`;
  writeFileSync(targetPath, finalContent, 'utf8');
}

const indexLines = files
  .map(file => basename(file, '.py'))
  .map(name => `export * from './${name}';`)
  .join('\n');

writeFileSync(join(targetDir, 'index.ts'), `${indexLines}\n`, 'utf8');
console.log(`Generated ${files.length} type files in ${targetDir}`);
